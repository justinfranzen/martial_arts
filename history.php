     <p>The history of martial arts around the world is complex. Most groups of people have had to defend themselves at some time and have developed fighting techniques for that purpose. Development of many martial arts was related to military development also. However, many of those techniques have been rendered technologically obsolete over the centuries. Even at an individual, rural level, the threat to the safety of a group of people is now more likely to come from modern weaponry such as automatic rifles than from men with swords. Furthermore, the preservation of a martial art requires many years of teaching at the hands of a good teacher to pass on the art for a single generation. So it is relatively unlikely that a particular martial art would survive and become popular in today's culture, and each art that has done so has a unique history.</p>
    
    <h2>EARLY HISTORY</h2>
        
        <br>
        
     <p>The teaching of martial arts in Asia has historically followed the cultural traditions of teacher-disciple apprenticeship. Students are trained in a strictly hierarchical system by a master instructor: Sifu in Cantonese, Lao Shih, lo sh (lit., old master) in Chinese; Shih fu, Sh fÃ¹ (lit., the master-father) in Mandarin; Guru in Hindi and Sanskrit; Sensei in Japanese; Kwan Jang Nim in (Korean). The instructor is expected to directly supervise their students' training, and the students are expected to memorize and recite as closely as possible the rules and basic training routines of the school. </p>
        
        <br>
    
     <p>In the warrior Kshatriya caste of India, organised martial traditions were studied as a part of the Dharma (duty) of the caste. The senior teachers were called Gurus and taught martial arts at gurukuls to young Kshatriyas. The examples of such Guru-shishya tradition (teacher-disciple tradition) is especially notable in case of Dronacharya, the guru to the Kauravas and the Pandavas. </p>
        
        <br>
        
    <p>Some method of certification can be involved, where one's skills would be tested for mastery before being allowed to study further; in some systems, especially in China, there may not have been any such certifications, only years of close personal practice and evaluation under a master, much like an apprenticeship, until the master deems one's skills satisfactory. This pedagogy, while still preserved and respected in many traditional styles, has weakened to varying degrees in others and is even actively rejected by some schools, especially in the West. </p>
    
        
    <h2>MODERN HISTORY</h2>
        
        <br>
    
    <p>The Western interest in East Asian Martial Arts dates back to the late 19th Century, due to the increase in trade between America and China and Japan. Relatively few Westerners actually practiced the arts, considering it to be mere performance. Many of the first demonstrations of the martial arts in the West were performed by Asians in vaudeville shows, which served to further reinforce the perception of the martial arts as dramatic performance. </p>
        
        <br>
        
    <p>As Western influence grew in the East a greater number of military personnel spent time in China, Japan, Korea and elsewhere. Exposure to martial arts during the Korean war was also significant. Gradually some soldiers began to see the value of Eastern martial arts and began training in them. </p>
        
        <br>
    
    <p>With large numbers of American servicemen stationed in Japan after World War II, the adoption of techniques and the gradual transmission of entire systems of martial arts to the West started. It was in the 1950's, however, when this exportation of systems really began to gain momentum. Large groups of U.S. Military personnel were taught Korean arts (Taekwondo) during the Korean conflict, and many of these brought their training home and continued to practice and teach after their demobilization. By the 1960s, Japanese arts like Karate and Judo had become very popular. The early 1970s saw martial arts movies, due in part to martial artist Bruce Lee, cause the rise in popularity of Chinese martial arts (kung fu). </p>
        
        <br>
    
    <p>This exportation of the martial arts led to such styles as sport karate, which became a major international sport, with professional fighters, big prizes, television coverage, and sponsorship deals. </p>
    
